_TIER2_REPRESENTATIVES = [
    "optimize-compress",
    "optimize-2048",
    "optimize-tsp",
    "optimize-codegen",
    "optimize-hash",
]


def _make_smoke_workspace(example_dir: Path, tmp_path: Path) -> Path:
    """Copy an example into a tmp dir and init a git repo so Orchestrator
    can write commits."""
    import shutil
    ws = tmp_path / example_dir.name
    shutil.copytree(example_dir, ws)
    subprocess.run(["git", "init", "-q"], cwd=ws, check=True)
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t", "add", "-A"],
        cwd=ws, check=True,
    )
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t",
         "commit", "-q", "-m", "init"],
        cwd=ws, check=True,
    )
    return ws


from crucible.agents.base import AgentInterface, AgentResult
from crucible.results import UsageInfo


class _SmokeAgent(AgentInterface):
    """Deterministic agent that touches every editable file with a no-op
    comment. Avoids triggering metric assertions; just exercises the
    orchestrator/ledger plumbing."""

    model = "smoke-agent"

    def __init__(self, editable_files: list[str]) -> None:
        self.editable_files = editable_files
        self.calls = 0

    def generate_edit(self, prompt: str, workspace: Path):
        self.calls += 1
        modified: list[Path] = []
        for rel in self.editable_files:
            target = workspace / rel
            if target.exists() and target.suffix == ".py":
                content = target.read_text()
                target.write_text(content + f"\n# smoke iter {self.calls}\n")
                modified.append(Path(rel))
                break  # only edit one file per iteration
        return AgentResult(
            modified_files=modified or [Path(self.editable_files[0])],
            description=f"smoke iter {self.calls}",
            usage=UsageInfo(total_cost_usd=0.0, num_turns=1),
        )


@pytest.mark.parametrize("example_name", _TIER2_REPRESENTATIVES)
def test_tier2_mocked_smoke_writes_ledger(example_name: str, tmp_path: Path):
    """Tier 2: single iteration through real Orchestrator with a mocked agent.

    Asserts the ledger.jsonl has at least one record and it's parseable.
    Does NOT assert metric correctness — the smoke agent's no-op comment
    may produce keep / discard / crash outcomes; what we care about is
    that the LEDGER got the entry and didn't crash the loop.
    """
    example_dir = _EXAMPLES_ROOT / example_name
    if not example_dir.exists():
        pytest.skip(f"example {example_name} not bundled")
    config = load_config(example_dir)
    ws = _make_smoke_workspace(example_dir, tmp_path)

    from crucible.orchestrator import Orchestrator

    agent = _SmokeAgent(config.files.editable)
    orchestrator = Orchestrator(
        config=config, workspace=ws, tag="smoke", agent=agent,
    )
    orchestrator.init()
    try:
        outcome = orchestrator.run_one_iteration()
    except Exception as exc:
        pytest.fail(f"orchestrator raised on {example_name}: {exc}")

    # Ledger must exist and have ≥1 entry
    ledger_path = ws / "logs" / "run-smoke" / "ledger.jsonl"
    assert ledger_path.exists(), (
        f"{example_name}: no ledger written after iteration "
        f"(outcome={outcome})"
    )
    nodes = TrialLedger(ledger_path).all_nodes()
    assert len(nodes) >= 1, f"{example_name}: ledger empty"
    # Sanity on first node
    n = nodes[0]
    assert n.id == "n000001"
    assert n.outcome in ("keep", "discard", "crash", "violation", "skip")



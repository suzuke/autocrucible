"""M1a demo gate CI per v1.0-design-final.md §5.1.

Three tiers, each forming an independent regression layer:

  Tier 1 — 22 example projects × config-load + ledger schema dry-run.
           Zero LLM tokens. Catches: schema regression, missing config
           fields, dataclass field renames.

  Tier 2 —  5 representative examples × 1 iteration through real
           orchestrator with deterministic FakeAgent. Catches: ledger
           dual-write breakage, parent_id chain bugs, integration with
           git/runner.

  Tier 3 —  2 examples × 3 real-agent iterations. Tagged @pytest.mark.expensive
           and `xfail` by default — opt-in via `pytest -m expensive`. Catches
           regressions that only surface with real LLM behavior.

Tier 1 + Tier 2 must pass before merging any PR that touches ledger /
orchestrator / reporter. Tier 3 is nightly / pre-release.
"""


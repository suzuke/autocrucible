def test_tier1_reporter_renders_for_empty_ledger(tmp_path: Path):
    out = render_static_html(tmp_path / "no-such.jsonl")
    assert "<html" in out
    assert "No attempts" in out


def test_tier1_reporter_renders_for_each_outcome_type(tmp_path: Path):
    """One synthetic ledger covering every declared outcome — reporter must
    not crash and must produce a valid HTML page."""
    ledger = TrialLedger(tmp_path / "ledger.jsonl")
    outcomes = ["keep", "discard", "crash", "violation", "skip",
                "budget_exceeded", "fatal"]
    for i, outcome in enumerate(outcomes, start=1):
        ledger.append_node(AttemptNode(
            id=AttemptNode.short_id(i),
            commit=f"sha-{i:08x}",
            outcome=outcome,
            cost_usd=0.001,
            usage_source="api",
            created_at="2026-04-25T12:00:00+00:00",
        ))
    html = render_static_html(tmp_path / "ledger.jsonl")
    assert "<html" in html
    for outcome in outcomes:
        assert outcome in html, f"reporter dropped outcome {outcome}"



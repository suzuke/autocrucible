_EXAMPLES_ROOT = Path(__file__).resolve().parents[1] / "src" / "crucible" / "examples"


def _example_dirs() -> list[Path]:
    """All `optimize-*` directories with a `.crucible/config.yaml`."""
    out = []
    for child in sorted(_EXAMPLES_ROOT.iterdir()):
        if child.is_dir() and (child / ".crucible" / "config.yaml").exists():
            out.append(child)
    return out


_ALL_EXAMPLES = _example_dirs()
_EXAMPLE_IDS = [d.name for d in _ALL_EXAMPLES]


# ---------------------------------------------------------------------------
# Tier 1 — config + schema dry-run for every bundled example
# ---------------------------------------------------------------------------


@pytest.mark.parametrize("example_dir", _ALL_EXAMPLES, ids=_EXAMPLE_IDS)
def test_tier1_example_config_loads(example_dir: Path):
    """Every bundled example's .crucible/config.yaml must load cleanly."""
    config = load_config(example_dir)
    assert config is not None
    # Required fields per ConfigError contract:
    assert config.metric.name
    assert config.metric.direction in ("maximize", "minimize")
    assert config.commands.run
    assert config.commands.eval
    assert config.files.editable, f"{example_dir.name}: no editable files declared"


# Examples whose editable files are entirely agent-created (no starter
# fixtures expected on disk). These are exempt from the editable-existence
# check because the agent is supposed to author them from scratch.
_AGENT_CREATES_ALL_EDITABLE = {
    "optimize-parameter-golf",  # train_gpt_mlx.py is generated by the agent
}


@pytest.mark.parametrize("example_dir", _ALL_EXAMPLES, ids=_EXAMPLE_IDS)
def test_tier1_editable_files_exist(example_dir: Path):
    """At least ONE declared editable path must exist on disk — otherwise
    the example is broken before the agent ever starts. Some examples
    intentionally declare multiple editables where only some are starter
    files (others are agent-created), so we require at least one to be
    present rather than all. Examples in `_AGENT_CREATES_ALL_EDITABLE` are
    exempt entirely."""
    if example_dir.name in _AGENT_CREATES_ALL_EDITABLE:
        pytest.skip(f"{example_dir.name}: agent-creates-all editable convention")
    config = load_config(example_dir)
    if not config.files.editable:
        pytest.skip(f"{example_dir.name}: no editable files declared")
    existing = [rel for rel in config.files.editable
                if (example_dir / rel).exists()]
    assert existing, (
        f"{example_dir.name}: NONE of declared editable files exist on disk: "
        f"{config.files.editable}"
    )


